package Ch4;

public class Array14 {
	//배열
	public static void main(String[] args) {
		//String 배열
		String[] name = new String[3]; // 3개의 문자열 담을 수 있는 배열 생성
		
		//초기화
		name[0] = "Kim";
		name[1] = "Park";
		name[2] = "Yi";
		/*
		 * 선언과 초기화 동시에 하는 방법
		 * String[] name = new String[] {"Kim", "Park", "Yi"}
		 * String[] name = {"Kim", "Park", "Yi"} 
		 */
	}

}
